
// Tipos de datos en JavaScript, llamado tipos primitivos

// numerico
let miEntero =  10;
console.log(miEntero);
console.log(typeof miEntero);

// decimal , js no distingue entre flotante , double y entero 
let miFlotante = 3.1416;
console.log(miFlotante);
console.log(typeof miFlotante);
// string
let miCadenas  = "Hola mundo";
console.log(miCadenas);
console.log(typeof miCadenas);
// boleano
let miBooleano = true; // true o false
console.log(miBooleano);
console.log(typeof miBooleano);

// ausencia de valor
let miNull = null;
console.log(miNull);
console.log(typeof miNull);

// undefined ausencia de valor mas usado
let miUndefined = undefined;
console.log(miUndefined);
console.log(typeof miUndefined);

// typeof nos dice el tipo de dato

console.log(typeof miBooleano);





