// Operdores de asignacion

let miNumero = 10;

console.log(miNumero);


miNumero = 20;
console.log(miNumero);

// operador de asignacion +=
miNumero  += 5;
console.log(miNumero);

// operador de asignacion -=

miNumero -= 3;
console.log(miNumero);

// operador de asiganion *=

miNumero *= 2;
console.log(miNumero);

// operador de asignacion /=

miNumero /= 4;
console.log(miNumero);

// operador de asignacion %=

miNumero %= 2;
console.log(miNumero);

// operador de asignacion **=
miNumero = 5;
miNumero **= 2;
console.log(miNumero);