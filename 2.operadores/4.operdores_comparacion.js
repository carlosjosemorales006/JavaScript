

// operadores de comparacion

let a = 5;
let b = '5';

console.log(a);
console.log(b);

// Operdores de igualda ==

console.log("a == b", a == b);
console.log(`${a} == ${b} -> ${a == b}`)

// Operador de igualdad estricta ===

console.log("a === b", a === b);

// string interpolation
console.log(`${a} == ${b} -> ${a == b}`);

// operador distintos
// compara valor y convierte el tipo de dato si es necesario

console.log(`${a} != ${b} -> ${a != b}`)

// operador distinto exacto

console.log(`${a} !== ${b} -> ${a !== b}`)

// operador menor que <

console.log(`${a} < ${b} -> ${a < b}`)

// menor o igual que 

console.log(`${a} <= ${b} -> ${a <= b}`)

// mayor que

console.log(`${a} > ${b} -> ${a > b }`)

// mayor o igual que 

console.log(`${a} >= ${b} -> ${a >= b }`)


