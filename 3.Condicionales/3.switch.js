
// sentencia switch

let diasemana = 8 ;

switch(diasemana){
    case 1: 
        console.log(`Dia ${diasemana} es Lunes`)
        break;
    case 2: 
        console.log(`Dia ${diasemana} es Martes`)
        break;
    case 3: 
        console.log(`Dia ${diasemana} es Miercoles`)
        break;
    case 4: 
        console.log(`Dia ${diasemana} es Jueves`)
        break;
    case 5:
        console.log(`Dia ${diasemana} es Viernes`)
        break;
    case 6:
         console.log(`Dia ${diasemana} es Sabado`)
        break;
    case 7: 
        console.log(`Dia ${diasemana} es Domingo`)
        break;
    default: 
        console.log(`Dia semana ${diasemana} es erroneo`)
     
}